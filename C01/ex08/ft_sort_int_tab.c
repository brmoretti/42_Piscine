void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	swap;

	i = -1;
	j = 0;
	while (i++ < size - 2)
	{
		j = i;
		while (j++ < size - 1)
		{
			if (tab[i] > tab[j])
			{
				swap = tab[i];
				tab[i] = tab[j];
				tab[j] = swap;
			}
		}
	}
}
