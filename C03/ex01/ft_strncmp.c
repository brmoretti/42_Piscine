int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (n == 0)
		return (0);
	while (*s1 == *s2 && *s1 != '\0' && *s2 != '\0' && n-- > 1)
	{
		s1++;
		s2++;
	}
	if (*s1 != '\0' && *s2 != '\0')
		return (*s1 - *s2);
	else if (*s1 != '\0')
		return (*s1);
	else
		return (-*s2);
}
